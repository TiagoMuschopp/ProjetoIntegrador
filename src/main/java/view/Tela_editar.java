      livro.setIsbn(isbn);
      livro.setAutor(autor);
      livro.setEditora(editora);
      livro.setPublicado(publicado);
      livro.setGenero(genero);
      livro.setPaginas(paginas);
      livro.setQtd_livros(qtd_livros);
   
      
        ControllerCadastro cad = new ControllerCadastro();
        try {
            
           cad.atualizarLivro(id, titulo, isbn, autor, editora, publicado, genero, paginas, qtd_livros);
            JOptionPane.showMessageDialog(null, "Livro atualizado com sucesso!");
        } catch (Exception ex) {
            System.out.println("Erro101");
            Logger.getLogger(Tela_editar.class.getName()).log(Level.SEVERE, null, ex);
        }
