      public void iniciarDados(int id){
        
    Livro livro = new Livro();
    livro = livro.exibirLivro(id); // Presumindo que o método exibirLivro está correto e retorna um objeto Livro

    if (livro != null) {
        jTextField1.setText(livro.getTitulo());
        jTextField2.setText(livro.getIsbn());
        jTextField3.setText(livro.getAutor());
        jTextField4.setText(livro.getEditora());
        jTextField5.setText(livro.getPublicado());
        jTextField6.setText(livro.getGenero());
        jSpinner1.setValue(livro.getPaginas());
        jSpinner2.setValue(livro.getQtd_livros());
    } else {
        System.out.println("O objeto livro é null");
    }
    }
